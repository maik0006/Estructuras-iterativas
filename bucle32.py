maikoll = 1
torres = 5 

while maikoll <= torres:
    fandiño = torres - maikoll
    daniel = 0

    while daniel < fandiño:
        print(" ", end="")
        daniel += 1

    torres1 = 0
    while torres1 < (2 * maikoll - 1):
        print("*", end="")
        torres1 += 1

    print()
    maikoll += 1
